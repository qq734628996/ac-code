int n, a[MAXN], d[MAXN][MAXN];

int dp(int i, int j, int day)
{
    if (i == j) return a[i] * day;
    if (d[i][j]) return d[i][j];
    return d[i][j] = max(a[i]*day+dp(i+1,j,day+1), a[j]*day+dp(i,j-1,day+1));
}

